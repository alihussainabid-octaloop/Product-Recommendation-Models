import transformers
print(transformers.__version__)

import os
import pickle
import pandas as pd
import torch

from google.colab import files
from dotenv import load_dotenv
from torch.utils.data import random_split
from transformers import (
    AutoTokenizer,
    AutoModelForSequenceClassification,
    TrainingArguments,
    Trainer,
    DataCollatorWithPadding,
    TrainerCallback
)

from sklearn.metrics import accuracy_score, precision_recall_fscore_support

# -----------------------------
# Load Data
# -----------------------------

def load_data(path: str) -> pd.DataFrame:
    return pd.read_csv(path)

# -----------------------------
# Torch Dataset
# -----------------------------

class TorchReviewDataset(torch.utils.data.Dataset):

    def __init__(self, encodings, labels):
        self.encodings = encodings
        self.labels = labels

    def __getitem__(self, idx):

        item = {
            key: torch.tensor(val[idx])
            for key, val in self.encodings.items()
        }

        item["labels"] = torch.tensor(self.labels[idx])
        return item

    def __len__(self):
        return len(self.labels)

# -----------------------------
# Metrics
# -----------------------------

def compute_metrics(eval_pred):

    logits, labels = eval_pred
    predictions = logits.argmax(axis=-1)

    precision, recall, f1, _ = precision_recall_fscore_support(
        labels,
        predictions,
        average="weighted"
    )

    accuracy = accuracy_score(labels, predictions)

    return {
        "accuracy": accuracy,
        "f1": f1,
        "precision": precision,
        "recall": recall
    }

# -----------------------------
# Pickle Checkpoint Callback
# -----------------------------

class PickleCheckpointCallback(TrainerCallback):

    def on_save(self, args, state, control, **kwargs):

        model = kwargs["model"]
        optimizer = kwargs["optimizer"]
        scheduler = kwargs["lr_scheduler"]

        checkpoint_dir = os.path.join(
            args.output_dir,
            f"checkpoint-{state.global_step}"
        )

        pickle_path = os.path.join(checkpoint_dir, "model_state.pkl")

        checkpoint_data = {
            "model_state_dict": model.state_dict(),
            "optimizer_state_dict": optimizer.state_dict(),
            "scheduler_state_dict": scheduler.state_dict(),
            "epoch": state.epoch,
            "global_step": state.global_step
        }

        with open(pickle_path, "wb") as f:
            pickle.dump(checkpoint_data, f)

        print(f"Saved pickle checkpoint → {pickle_path}")

