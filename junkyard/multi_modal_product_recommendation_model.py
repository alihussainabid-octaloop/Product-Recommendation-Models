DATASET_PATH = "/content/drive/MyDrive/Datasets/Processed/amazon_product_reviews_sentiment_preprocessed.csv"
MODEL_NAME = "google-bert/bert-base-uncased"
OUTPUT_DIR = "/content/trained_model/"

# Load dataset
df = load_data(DATASET_PATH)

# limit dataset (optional)
df = df.head(5000)

texts = df["Text"].tolist()
labels = df["Label"].tolist()

# label encoding
labels_map = {
    "negative": 0,
    "neutral": 1,
    "positive": 2
}

labels = [labels_map[label] for label in labels]

# -----------------------------
# Tokenizer + Model
# -----------------------------

tokenizer = AutoTokenizer.from_pretrained(MODEL_NAME)

model = AutoModelForSequenceClassification.from_pretrained(
    MODEL_NAME,
    num_labels=3
)

# -----------------------------
# Tokenization
# -----------------------------

encodings = tokenizer(
    texts,
    truncation=True,
    max_length=512
)


dataset = TorchReviewDataset(encodings, labels)

# -----------------------------
# Train / Validation Split
# -----------------------------

train_size = int(0.8 * len(dataset))
val_size = len(dataset) - train_size

train_dataset, val_dataset = random_split(
    dataset,
    [train_size, val_size],
    generator=torch.Generator().manual_seed(42)
)

# -----------------------------
# Training Arguments
# -----------------------------

training_args = TrainingArguments(

    output_dir=OUTPUT_DIR,

    learning_rate=2e-5,

    per_device_train_batch_size=8,
    per_device_eval_batch_size=8,

    num_train_epochs=15,

    eval_strategy="epoch",
    save_strategy="epoch",

    load_best_model_at_end=True,
    metric_for_best_model="f1",

    logging_dir="./logs",

    report_to="none"
)

# -----------------------------
# Data Collator
# -----------------------------

data_collator = DataCollatorWithPadding(tokenizer=tokenizer)

# -----------------------------
# Trainer
# -----------------------------

trainer = Trainer(

    model=model,
    args=training_args,

    train_dataset=train_dataset,
    eval_dataset=val_dataset,

    data_collator=data_collator,

    compute_metrics=compute_metrics,

    callbacks=[PickleCheckpointCallback()]
)

# -----------------------------
# Train
# -----------------------------

trainer.train()


# -----------------------------
# Save Final Model
# -----------------------------

trainer.save_model(OUTPUT_DIR)
tokenizer.save_pretrained(OUTPUT_DIR)


# -----------------------------
# Save Final Pickle
# -----------------------------

final_pickle = os.path.join(
    OUTPUT_DIR,
    "final_model_state.pkl"
)

final_state = {
    "model_state_dict": trainer.model.state_dict(),
    "training_args": training_args
}

with open(final_pickle, "wb") as f:
    pickle.dump(final_state, f)

